#include<stdio.h>
int main ()
{
	int a,b,c,d,e,f;
	scanf("%d%d", &a,&b);
	c=a+b;
	d=a-b;
	e=a*b;
	f=a/b;
	printf("The sum of the given numbers is %d\n",c);
	printf("The difference of the given numbers is %d\n",d);
	printf("The multiplication of the given numbers is %d\n",e);
	printf("The division of the given numbers is %d\n",f);
	return 0;	
}
